const fs = require('fs');
const path = 'apps/web/src/pages/DashboardPage.tsx';
let text = fs.readFileSync(path, 'utf8');
const blockStart = '            {(() => {\r\n';
const blockEnd = '            })()}\r\n';
const start = text.indexOf(blockStart);
if (start === -1) throw new Error('block start not found');
const end = text.indexOf(blockEnd, start);
if (end === -1) throw new Error('block end not found');
const body = text.slice(start + blockStart.length, end);
const replacement = '            {activeTradesDetailContent}\r\n';
text = text.slice(0, start) + replacement + text.slice(end + blockEnd.length);
const returnMarker = '\r\n  return (\r\n';
const returnIndex = text.indexOf(returnMarker);
if (returnIndex === -1) throw new Error('return marker not found');
const injection = '\r\n  const activeTradesDetailContent = (() => {\r\n' + body + '  })();\r\n';
text = text.slice(0, returnIndex) + injection + text.slice(returnIndex);
fs.writeFileSync(path, text, 'utf8');
console.log('extracted active trades detail content');
